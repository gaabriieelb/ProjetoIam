    private void buttonFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonFinalizarActionPerformed
        String mes = (String) comboMes.getSelectedItem();
        String numMes = "";
        String ano = fieldAno.getText();
        String diaInicial = fieldDiaInicial.getText();
        String diaFinal = fieldDiaFinal.getText();
        
                switch(mes){
                    case "Janeiro": 
                        numMes="01";
                        break;
                        
                    case "Fevereiro": 
                        numMes="02";
                        break;    
                    
                    case "Março": 
                        numMes="03";
                        break;
                        
                    case "Abril": 
                        numMes="04";
                        break;
                        
                    case "Maio": 
                        numMes="05";
                        break;
                        
                    case "Junho": 
                        numMes="06";
                        break;
                        
                    case "Julho": 
                        numMes="07";
                        break;
                        
                    case "Agosto": 
                        numMes="08";
                        break;    
                    
                    case "Setembro": 
                        numMes="09";
                        break;
                        
                    case "Outubro": 
                        numMes="10";
                        break;
                        
                    case "Novembro": 
                        numMes="11";
                        break;
                        
                    case "Dezembro": 
                        numMes="12";
                        break;
                }
        
        String dataInicial = diaInicial+"/"+numMes+"/"+ano;
        String dataFinal = diaFinal+"/"+numMes+"/"+ano;
        
        RelatorioData relatorioData = new RelatorioData();
        try {
            relatorioData.gerarFaturamentoPeriodo(dataInicial, dataFinal);
        } catch (SQLException ex) {
            Logger.getLogger(RelatorioFaturamentoPeriodo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_buttonFinalizarActionPerformed

