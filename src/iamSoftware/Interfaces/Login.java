    public int Login(String usuario, String senha) throws SQLException{
       
        int id = 0;
        
        String sql = "SELECT * FROM `users` WHERE username='"+usuario+"' AND password=MD5('"+senha+"')";
        
        Connection conn = ConexaoBD.Conectar();           
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();
                    
        while(rs.next()){
            id = rs.getInt("id");
            
        }
        return id;
    }
    
    public String getNivel(int id) throws SQLException{
       
        String nivel="";
        
        String sql = "SELECT * FROM `users` WHERE id="+id;
        
        Connection conn = ConexaoBD.Conectar();           
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();
                    
        while(rs.next()){
            nivel = rs.getString("nivelAcesso");
            
        }
        return nivel;
    }

